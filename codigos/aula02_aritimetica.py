# Operações aritemeticas
# Somar, subtrair, multiplicar, dividir.

somar = 8 + 7
print(somar)

x = 15
y = 12

somar = x + y
print(somar)

subtracao = 12 - 8

print(subtracao)

subtracao = 6 - 15

print(subtracao)

multiplicacao = 3 * 6

print(multiplicacao)

divisao = 10 / 4

print(divisao)

# divisão com resultado inteiro
divisao_inteira = 10 // 4 # o // serve para trazer o resultado inteiro
                          # o // não arrendonda o resultado, traz somente inteiro.
print(divisao_inteira)

resto = 25 % 7

print(resto)

potenciacao = 3 ** 2

print(potenciacao)